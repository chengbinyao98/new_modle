import pandas as pd

# 读取csv结果文件, 输出为pandas.Dataframe
results = pd.read_csv("nine_periods.csv")
print("读取csv结果文件, 输出为pandas.Dataframe")
print(results)
print("\n\n")

# 读取txt结果文件, 输出为二维list, 第一维是行, 第二维是值.
with open("nine_periods.txt", "r") as f:
    results_in_txt = []
    s = 0
    for i in f.readlines():
        if s != 0:
            text_list = []
            text = i.replace("\n", "")
            for j in text.split(","):
                text_list.append(float(j))
            results_in_txt.append(text_list)
        s += 1
print("读取txt结果文件, 输出为二维list, 第一维是行, 第二维是值.")
print("数据结果值按这个顺序排列mu,s_sigma,d_sigma,main1,main2,main,one_agent,direct")
print(results_in_txt)
print("\n\n")

# 变量结果为二维列表(list),直接复制results_total_list的结果即可使用,第一维是行,第二维是值.
results_total_list = results.values.tolist()
print("变量结果为二维列表(list),直接复制results_total_list的结果即可使用,第一维是行,第二维是值.")
print(results_total_list)
print("\n\n")

# 输出具体(结果框内文字直接复制使用)为字典(dict),key是页和时段,value是值.
print("输出具体(结果框内文字直接复制使用)为字典(dict),key是页和时段,value是值.")
j = 0
for i in results_total_list:
    page = 0
    period = 0
    if j < 15:
        page = 1
        period = j % 5 + 1
    elif j < 27:
        page = 2
        period = (j - 15) % 4 + 1
    else:
        page = 3
        period = (j - 27) % 4 + 1
    if j < 38:
        print('"第{}页,时段{}": {},'.format(page, period, i))
    else:
        print('"第{}页,时段{}": {}'.format(page, period, i))
    j += 1
print("\n\n")

# 输出具体(结果框内文字直接复制使用)为多条列表(list),page_x_period_y变量名是页和时段,=后面是值.
print("输出具体(结果框内文字直接复制使用)为多条列表(list),page_x_period_y变量名是页和时段,=后面是值.")
j = 0
for i in results_total_list:
    page = 0
    period = 0
    if j < 15:
        page = 1
        period = j % 5 + 1
    elif j < 27:
        page = 2
        period = (j - 15) % 4 + 1
    else:
        page = 3
        period = (j - 27) % 4 + 1
    print('page_{}_period_{} = {}'.format(page, period, i))
    j += 1
